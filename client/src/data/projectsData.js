/**
 * Get project by slug
 * @param {string} slug - The project slug
 * @returns {object} The project object or null
 */
export const getProjectBySlug = (slug) => {
  return projectsData.find(project => project.slug === slug) || null;
};

/**
 * Get projects by category
 * @param {string} category - The project category
 * @returns {array} Array of projects in the category
 */
export const getProjectsByCategory = (category) => {
  return projectsData.filter(project => project.category === category);
};

/**
 * Get all project categories
 * @returns {array} Array of unique categories
 */
export const getProjectCategories = () => {
  return [...new Set(projectsData.map(project => project.category))];
};

/**
 * Get latest projects
 * @param {number} count - Number of projects to return
 * @returns {array} Array of latest projects
 */
export const getLatestProjects = (count = 3) => {
  return projectsData.slice(0, count);
};

export default projectsData;
