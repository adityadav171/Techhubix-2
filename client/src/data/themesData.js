/**
 * Get all unique theme categories
 * @returns {Array} Array of unique category names
 */
export const getThemeCategories = () => {
  const categories = themesData.map(theme => theme.category);
  return [...new Set(categories)].sort();
};

/**
 * Get a single theme by slug
 * @param {string} slug - Theme slug identifier
 * @returns {Object|null} Theme object or null if not found
 */
export const getThemeBySlug = (slug) => {
  return themesData.find(theme => theme.slug === slug) || null;
};

/**
 * Get themes by category
 * @param {string} category - Category name
 * @returns {Array} Array of themes in the category
 */
export const getThemesByCategory = (category) => {
  if (category === 'All') return themesData;
  return themesData.filter(theme => theme.category === category);
};
